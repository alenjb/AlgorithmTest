SELECT A.ANIMAL_ID, A.NAME
FROM ANIMAL_OUTS A
WHERE A.ANIMAL_ID NOT IN (SELECT B.ANIMAL_ID FROM ANIMAL_INS B)
ORDER BY A.ANIMAL_ID















