
SELECT R.NAME, R.DATETIME
FROM ANIMAL_INS R
WHERE R.ANIMAL_ID NOT IN (SELECT ANIMAL_ID FROM ANIMAL_OUTS)
ORDER BY R.DATETIME
LIMIT 3










