SELECT 
    A.ID,
    IFNULL((
        SELECT COUNT(*)
        FROM ECOLI_DATA B
        WHERE B.PARENT_ID = A.ID
    ), 0) AS CHILD_COUNT
FROM ECOLI_DATA A;












